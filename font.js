const characters = text => {
  var result = [];
  for (var index = 0; index < text.length; ++index) {
    var code = text.charCodeAt(index) - 32;
    if (code >= 0 && code < charactersByCode.length) {
      var character = charactersByCode[code];
      result.push(character);
    } else {
      result.push(space);
    }
  }
  return result;
};

